function aitPlay(soundName) {
    const soundsStatus = localStorage.getItem("epsilonSounds")
    if (soundsStatus === 'true' || !soundsStatus) {
        if (aitSounds[soundName]) {  // Check if the sound exists in the map
            try {
                aitSounds[soundName].play();  // Play the sound
            } catch (error) {
                console.warn("Error playing sound.", error);
            }
        } else {
            console.warn("Sound not found:", soundName);
        }
    } else {
        console.log(`Sounds are disabled. Not playing ${soundName}`)
    }

}
